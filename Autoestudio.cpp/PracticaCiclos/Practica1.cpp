/*Hacer un programa que imprima
los numeros del 1 al 100*/

#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
   int num = 0;

    while (num < 100)
    {
        cout << num << endl;
        num+=2;
    }
    
    
    return 0;
}

